// Function for animate progress bars
function animateProgressBarsOnScroll() {
    const section = document.querySelector('.d-flex.flex-column.gap-3');
    const bars = section.querySelectorAll('.progress-bar');
    let animationFrame;

    function animateBar(bar, target, duration) {
        let start = null;
        const initial = 0;
        function easeOutElastic(t) {
            const c4 = (2 * Math.PI) / 3;
            return t === 0
                ? 0
                : t === 1
                ? 1
                : Math.pow(2, -10 * t) * Math.sin((t * 10 - 0.75) * c4) + 1;
        }
        function animate(ts) {
            if (!start) start = ts;
            let elapsed = ts - start;
            let progress = Math.min(elapsed / duration, 1);
            let eased = easeOutElastic(progress);
            let value = Math.round(initial + (target - initial) * eased);
            bar.style.width = value + '%';
            bar.textContent = value + '%';
            if (progress < 1) {
                animationFrame = requestAnimationFrame(animate);
            } else {
                bar.textContent = target + '%';
            }
        }
        animationFrame = requestAnimationFrame(animate);
    }

    function resetBar(bar) {
        bar.style.width = '0%';
        bar.textContent = '...';
    }

    const observer = new IntersectionObserver(entries => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                bars.forEach(bar => {
                    const target = parseInt(bar.getAttribute('data-progress'), 10);
                    // Random duration between 1000ms and 2000ms
                    const duration = 1000 + Math.random() * 2000;
                    animateBar(bar, target, duration);
                });
            } else {
                bars.forEach(bar => {
                    resetBar(bar);
                });
            }
        });
    }, { threshold: 0.4 });

    observer.observe(section);
}

