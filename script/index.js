// Function to format time in a readable way
function formatTime(time) {
    const { seconds, minutes, hours, days } = time;

    // Less than 1 minute
    if (seconds < 60) {
        return seconds === 1 ? '1 second ago' : `${seconds} seconds ago`;
    }
    // Less than 1 hour
    else if (minutes < 60) {
        return minutes === 1 ? '1 minute ago' : `${minutes} minutes ago`;
    }
    // Less than 1 day
    else if (hours < 24) {
        return hours === 1 ? '1 hour ago' : `${hours} hours ago`;
    }
    // Less than 30 days
    else if (days < 30) {
        return days === 1 ? '1 day ago' : `${days} days ago`;
    }
    // Less than 365 days
    else if (days < 365) {
        const months = Math.floor(days / 30);
        const remainingDays = days % 30;
        if (remainingDays === 0) {
            return months === 1 ? '1 month ago' : `${months} months ago`;
        }
        return `${months} month${months !== 1 ? 's' : ''} ${remainingDays} day${remainingDays !== 1 ? 's' : ''} ago`;
    }
    // More than 365 days
    else {
        const years = Math.floor(days / 365);
        const remainingDays = days % 365;
        if (remainingDays === 0) {
            return years === 1 ? '1 year ago' : `${years} years ago`;
        }
        const months = Math.floor(remainingDays / 30);
        const remainingDaysAfterMonths = remainingDays % 30;
        if (months === 0) {
            return `${years} year${years !== 1 ? 's' : ''} ${remainingDaysAfterMonths} day${remainingDaysAfterMonths !== 1 ? 's' : ''} ago`;
        }
        return `${years} year${years !== 1 ? 's' : ''} ${months} month${months !== 1 ? 's' : ''} ago`;
    }
}

// Function to calculate time difference
function calculateTimeSince(dateString) {
    const releaseDate = new Date(dateString);
    const today = new Date();
    const diff = today - releaseDate;
    
    // Calculate different time units
    const seconds = Math.floor(diff / 1000);
    const minutes = Math.floor(seconds / 60);
    const hours = Math.floor(minutes / 60);
    const days = Math.floor(hours / 24);
    
    return {
        seconds,
        minutes,
        hours,
        days
    };
}

